public class PrintOhneNewline {
    public static void main(String[] args) {
        // Drei print()-Befehle, um Wörter zusammenzufügen
        System.out.print("Java ");
        System.out.print("ist ");
        System.out.print("cool!");
        // Optional für Aufgabe
        System.out.print("\n"); // Manuell neue Zeile am Ende
    }
}
