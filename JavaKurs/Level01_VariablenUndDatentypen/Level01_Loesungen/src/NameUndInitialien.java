public class NameUndInitialien {

    public static void main(String[] args) {
        String vorname = "Max";
        String nachname = "Ferres";

        char initialieVorname = 'M';
        char initialieNachname = 'F';

        //Mein Vorname ist Max und beginnt mit M
        //Mein Nachname ist Ferres und beginnt mit F
        System.out.println("Mein Vorname ist " + vorname + " und beginnt mit " + initialieVorname);
        System.out.println("Mein Nachname ist " + nachname + " und beginnt mit " + initialieNachname);
    }

}
